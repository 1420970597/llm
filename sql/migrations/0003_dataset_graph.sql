ALTER TABLE datasets
  ADD COLUMN IF NOT EXISTS strategy_id BIGINT DEFAULT 0,
  ADD COLUMN IF NOT EXISTS provider_id BIGINT DEFAULT 0,
  ADD COLUMN IF NOT EXISTS storage_profile_id BIGINT DEFAULT 0,
  ADD COLUMN IF NOT EXISTS estimate_json JSONB NOT NULL DEFAULT '{}'::jsonb,
  ADD COLUMN IF NOT EXISTS updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW();

CREATE TABLE IF NOT EXISTS domains (
  id BIGSERIAL PRIMARY KEY,
  dataset_id BIGINT NOT NULL REFERENCES datasets(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  canonical_name TEXT NOT NULL,
  level INTEGER NOT NULL DEFAULT 1,
  parent_id BIGINT NULL,
  source TEXT NOT NULL DEFAULT 'ai',
  review_status TEXT NOT NULL DEFAULT 'draft',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS domain_edges (
  id BIGSERIAL PRIMARY KEY,
  dataset_id BIGINT NOT NULL REFERENCES datasets(id) ON DELETE CASCADE,
  source_domain_id BIGINT NOT NULL REFERENCES domains(id) ON DELETE CASCADE,
  target_domain_id BIGINT NOT NULL REFERENCES domains(id) ON DELETE CASCADE,
  relation_type TEXT NOT NULL DEFAULT 'contains',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
